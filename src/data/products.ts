/** Sensible defaults for a light, stackable, non-fragile packaged item. */
function handling(overrides: Partial<HandlingProfile> = {}): HandlingProfile {
  return {
    unitWeightKg: 1,
    packagedLengthCm: 30,
    packagedWidthCm: 20,
    packagedHeightCm: 15,
    stackable: true,
    maxStackQty: 20,
    fragile: false,
    uprightOnly: false,
    oversized: false,
    hazardous: false,
    palletised: false,
    palletLengthCm: null,
    palletWidthCm: null,
    palletHeightCm: null,
    qtyPerPallet: null,
    craneRequired: false,
    forkliftRequired: false,
    coveredVehicleRequired: false,
    openTruckAllowed: true,
    ...overrides,
  }
}

let vSeq = 0
function variant(
  productId: string,
  sku: string,
  name: { en: string; ar: string },
  priceMinor: number,
  attributes: ProductVariant['attributes'],
  h: HandlingProfile,
  extra: Partial<ProductVariant> = {},
): ProductVariant {
  vSeq += 1
  return {
    id: `var-${productId}-${vSeq}`,
    productId,
    sku,
    barcode: `62900${String(vSeq).padStart(7, '0')}`,
    name,
    priceMinor,
    promoPriceMinor: null,
    costMinor: Math.round(priceMinor * 0.7),
    taxRatePct: 15,
    stockStatus: 'in_stock',
    availableQty: 500,
    minQty: 1,
    maxQty: null,
    qtyIncrement: 1,
    imageUrl: null,
    attributes,
    handling: h,
    ...extra,
  }
}

